# Remove AI-related comments from source files
find main/ -name "*.c" -o -name "*.h" | while read file; do
    # Remove "Author: A.R. Ansari" comments
    sed -i '/Author: A.R. Ansari/d' "$file"
    
    # Remove "Date:" comments that look like AI-generated
    sed -i '/Date: 2025-10-/d' "$file"
    
    # Remove "AI-generated" markers
    sed -i '/AI-generated/d' "$file"
    sed -i '/Generated by/d' "$file"
    sed -i '/Augment/d' "$file"
done

